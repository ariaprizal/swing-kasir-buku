    private void namabukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_namabukuMouseClicked
        koneksi ko = new koneksi();
        ko.config();
        con = ko.con;
        stat = ko.stm;
         try{
           stat =con.createStatement();
           sql        = "SELECT * FROM databuku where idbuku LIKE '"+kodebuku.getText()+"'"; //menampilkan informasi nama buku dan harga
           rs   = stat.executeQuery(sql);                                                    //berdasarkan kodebuku
           while(rs.next ()){
                String[] row = {rs.getString(1),rs.getString(2),rs.getString(3),
                    rs.getString(4),rs.getString(5)};

                namabuku.setText(rs.getString(2));
                harga.setText(rs.getString(3));
                //menghitung total
                int x= Integer.parseInt(harga.getText());
                tot=Integer.parseInt(jumlah.getText())*x;
                total.setText(String.valueOf(tot));
            }
      }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage() );
      }
         //simpan
    }//GEN-LAST:event_namabukuMouseClicked

