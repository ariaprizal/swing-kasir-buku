       kembali.setText("Rp. "+kem);
        try{

        koneksi DB = new koneksi();
        DB.config();
        con = DB.con;
        stat = DB.stm;
                sql="insert into datapembelian (id,idbuku,jumlah,namabuku,total)" // query 
                  + "values(?,?,?,?,?)";
                pst=con.prepareStatement(sql);
                pst.setString(1, idbeli.getText().trim());
                pst.setString(2, kodebuku.getText().trim());
                pst.setString(3, jumlah.getText().trim());
                pst.setString(4, namabuku.getText().trim());
                pst.setString(5, total.getText().trim());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Telah Tersimpan");
         //reset
        idbeli.setText("");
        kodebuku.setText("");
        namabuku.setText("");
        jumlah.setText("");
        total.setText("");
        harga.setText("");
        bayar.setText("");
        kembali.setText("");
           
        }  catch (SQLException e){
    JOptionPane.showMessageDialog(null,"Gagal Mengisi data"+ e.getMessage());
}
       
